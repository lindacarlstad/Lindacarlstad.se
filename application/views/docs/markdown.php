Markdown dokumentation
===========================

Markdown är en text till HTML lösning skapad av [daringfireball](http://daringfireball.net).
Markdown klarar själv av att formatera texten du skriver och kan även skapa avancerade formateringar med hjälp av dig som användare!

<h3>Länkar</h3>
	An [example](http://url.com/ "Title")

<h3>Bilder</h3>
	![alt text](/path/img.jpg "Title")
	
<h3>titlar</h3>
	Header 1
	========

	Header 2
	--------

<h3>Lister</h3>
Ordnad

	1.  Foo
	2.  Bar

O-ordnad

	*   A list item.

    With multiple paragraphs.

	*   Bar

<h3>kodblock</h3>
	This is a normal paragraph.

    	This is a preformatted
    	code block.
    	
<h3>Övrigt</h3>
Kursiv

	*Kursiv text*
	_Kursiv text_
Fet

	**Fet text**
	__Fet text__

Du kan läsa den fullständiga dokumentationen på [daringfireball.net](http://daringfireball.net/projects/markdown/syntax).
